namespace ARS_ProjectSystem.Test
{
    using OpenQA.Selenium;
    using OpenQA.Selenium.Chrome;
    using Xunit;

    public class SeleniumTests
    {
        [Fact]
        public void H2ElementIsRemovedFromCustomersAllPage()
        {
            var serverFactory = new SeleniumServerFactory<Startup>();
            var options = new ChromeOptions();
            options.AddArgument("--headless");
            options.AcceptInsecureCertificates = true;
            var webDriver = new ChromeDriver(options);
            webDriver.Navigate().GoToUrl(serverFactory.RootUri+"/Customers/All");
            var element = webDriver.FindElement(By.TagName("h2"));
            Assert.Equal("All Customers", element.Text);
        }
    }
}
